
import java.util.*;
public class even_odd {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("ENTER THE NUMBER.\t");
        int n=sc.nextInt();
        if(n%2==0){
            System.out.println("EVEN NUMBER");
        }
        else{
            System.out.println("NOT EVEN NUMBER");
        }
    }
    
}
